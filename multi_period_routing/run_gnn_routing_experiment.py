def run_experiment(instances_dir: str, n_repeats: int, sa_kwargs: dict,
                   area_filter: str = None):
    """
    Iterate over area instance directories, run 3 SA modes × n_repeats per period.

    Parameters
    ----------
    area_filter : str or None
        If given, only process the area directory whose name matches this slug.
        If None, all areas are processed.

    Returns a list of result dicts suitable for concatenating into a DataFrame.
    """
    modes = ['baseline', 'exact_cache', 'intelligent_memory']
    all_results = []

    all_area_dirs = sorted([
        d for d in os.listdir(instances_dir)
        if os.path.isdir(os.path.join(instances_dir, d))
    ])
    area_dirs = [d for d in all_area_dirs
                 if area_filter is None or d == area_filter]

    if not area_dirs:
        log.error(f"No instance directories found in {instances_dir} "
                  f"(filter='{area_filter}'). "
                  "Run generate_gnn_routing_instances.py first.")
        return all_results

    for area_slug in area_dirs:
        area_path = os.path.join(instances_dir, area_slug)
        graph_pkl  = os.path.join(area_path, 'graph.pickle')
        edge_npy   = os.path.join(area_path, 'edge_probs.npy')
        meta_npy   = os.path.join(area_path, 'meta.npy')

        if not (os.path.exists(graph_pkl) and os.path.exists(edge_npy) and os.path.exists(meta_npy)):
            log.warning(f"[{area_slug}] Missing files – skipping.")
            continue

        # Load instance
        with open(graph_pkl, 'rb') as fh:
            G = pickle.load(fh)

        edge_probs_all = np.load(edge_npy, allow_pickle=True).item()
        meta = np.load(meta_npy, allow_pickle=True).item()

        area_name     = meta['area']
        capacity      = float(meta['capacity'])
        recharge_time = float(meta['recharge_time'])   # = 1.5 × capacity from generator
        T_interval   = float(meta['T_interval'])
        depot_nodes  = list(meta['depot_nodes'])
        n_periods    = int(meta['n_periods'])

        # Filter depots to nodes that actually exist in G
        valid_nodes  = set(G.nodes())
        depot_nodes  = [d for d in depot_nodes if d in valid_nodes]
        if not depot_nodes:
            # Fall back: pick highest-degree nodes
            degree = dict(G.degree())
            n_dep = max(2, G.number_of_nodes() // 15)
            depot_nodes = sorted(degree, key=degree.get, reverse=True)[:n_dep]

        max_flights = max(1, int(T_interval // max(recharge_time + capacity, 1e-9)))

        # Pre-compute shortest paths (using travel-time weights)
        log.info(f"\n{'='*60}")
        log.info(f"Area: {area_name} | nodes={G.number_of_nodes()} | "
                 f"edges={G.number_of_edges()} | depots={len(depot_nodes)} | "
                 f"periods={n_periods}")
        log.info(f"capacity={capacity:.0f}s | recharge={recharge_time:.0f}s | "
                 f"T_interval={T_interval:.0f}s | max_flights={max_flights}")
        log.info("Pre-computing shortest paths...")
        dist = dict(nx.all_pairs_dijkstra_path_length(G, weight='weight'))

        # ── Reachability filter ────────────────────────────────────────────────
        # Drop edges no drone can physically service in one flight.
        # Min feasible trip for edge (u,v): best_depot→u + w(u,v) + v→best_depot
        # If this > capacity, the SA solver would assert-fail on it → remove it.
        reachable_edge_keys: set = set()
        for edge_key in list(edge_probs_all.keys()):
            raw_key = edge_key
            if isinstance(raw_key, str):
                try:
                    raw_key = eval(raw_key)
                except Exception:
                    continue
            u, v = raw_key[0], raw_key[1]
            if not (G.has_node(u) and G.has_node(v)):
                continue
            min_to_u   = min(dist.get(d, {}).get(u, float('inf')) for d in depot_nodes)
            min_to_v   = min(dist.get(d, {}).get(v, float('inf')) for d in depot_nodes)
            w_uv = G[u][v].get('weight', float('inf')) if G.has_edge(u, v) else float('inf')
            w_vu = G[v][u].get('weight', float('inf')) if G.has_edge(v, u) else float('inf')
            min_v_back = min(dist.get(v, {}).get(d, float('inf')) for d in depot_nodes)
            min_u_back = min(dist.get(u, {}).get(d, float('inf')) for d in depot_nodes)
            trip_fwd   = min_to_u + w_uv + min_v_back
            trip_rev   = min_to_v + w_vu + min_u_back
            if min(trip_fwd, trip_rev) <= capacity + 1e-6:
                reachable_edge_keys.add(edge_key)

        n_total    = len(edge_probs_all)
        edge_probs_all = {k: v for k, v in edge_probs_all.items() if k in reachable_edge_keys}
        log.info(f"  Reachability filter: {len(edge_probs_all)}/{n_total} edges "
                 f"reachable within {capacity:.0f}s capacity")


        # ── Run modes ──────────────────────────────────────────────────────────
        for mode in modes:
            log.info(f"--- Mode: {mode.upper()} | Area: {area_slug} ---")

            for repeat in range(n_repeats):
                log.info(f"  Repeat {repeat+1}/{n_repeats}")

                # Long-term memory bank resets per repeat, persists across periods
                LTM_MAX_SIZE  = 100
                LTM_TOP_K     = 20
                CACHE_SIZE    = 500

                ltm = LongTermMemoryBank(max_per_depot=LTM_MAX_SIZE) if mode == 'intelligent_memory' else None

                for period in range(n_periods):
                    t0 = time.time()

                    # Build per-period edge probabilities dict
                    edge_probs: Dict[Tuple, float] = {}
                    for edge_key, prob_list in edge_probs_all.items():
                        p = float(prob_list[period]) if period < len(prob_list) else 0.0
                        # edge_key might be stored as tuple or as a numpy/string – normalise
                        if isinstance(edge_key, str):
                            try:
                                edge_key = eval(edge_key)
                            except Exception:
                                continue
                        canon = (min(edge_key[0], edge_key[1]), max(edge_key[0], edge_key[1]))
                        edge_probs[canon] = p

                    # Cache / LTM setup
                    if mode == 'baseline':
                        trip_cache = DummyTripCache()
                    else:
                        trip_cache = TripCache(max_size=CACHE_SIZE)

                    seeded_count = 0
                    if ltm is not None and period > 0:
                        selected = ltm.rescore_and_select(period, edge_probs, top_k=LTM_TOP_K)
                        seeded_count = ltm.seed_trip_cache(trip_cache, selected, set(depot_nodes), G)

                    # Run SA
                    if mode == 'baseline':
                        best_trips, best_obj, best_prob, best_beta, _ = simulated_annealing_magnetic_no_cache(
                            G, dist, depot_nodes, edge_probs,
                            capacity, recharge_time, T_interval, max_flights,
                            trip_cache={}, **sa_kwargs
                        )
                    else:
                        best_trips, best_obj, best_prob, best_beta = simulated_annealing_magnetic(
                            G, dist, depot_nodes, edge_probs,
                            capacity, recharge_time, T_interval, max_flights,
                            trip_cache=trip_cache, **sa_kwargs
                        )

                    runtime = time.time() - t0

                    if ltm is not None:
                        ltm.merge_trips(period, best_trips, edge_probs, G)
                        ltm.prune(period)

                    # Coverage metrics
                    total_prob = sum(edge_probs.values())
                    covered_edges = set()
                    for vehicle_routes in best_trips:
                        for route in vehicle_routes:
                            for i in range(len(route) - 1):
                                u, v = route[i], route[i+1]
                                if u != v:
                                    canon = (min(u, v), max(u, v))
                                    if canon in edge_probs:
                                        covered_edges.add(canon)

                    covered_prob = sum(edge_probs[e] for e in covered_edges)
                    pct_covered  = 100.0 * covered_prob / total_prob if total_prob > 0 else 0.0

                    hits     = trip_cache.hits
                    misses   = trip_cache.misses
                    accepts  = getattr(trip_cache, 'hits_accepted', 0)
                    rejects  = getattr(trip_cache, 'hits_rejected', 0)
                    ltm_entries = sum(len(c) for c in ltm.entries.values()) if ltm else 0
                    promoted    = ltm.total_promoted if ltm else 0
                    pruned      = ltm.total_pruned if ltm else 0

                    rec = {
                        'area': area_name,
                        'mode': mode,
                        'repeat': repeat,
                        'period': period,
                        'obj': round(best_obj, 6),
                        'prob': round(best_prob, 6),
                        'beta': round(best_beta, 3),
                        'time_s': round(runtime, 3),
                        'unique_edges_covered': len(covered_edges),
                        '%_covered': round(pct_covered, 2),
                        'capacity_s': capacity,
                        'T_interval_s': T_interval,
                        'n_depots': len(depot_nodes),
                        'max_flights': max_flights,
                        'cache_hits': hits,
                        'cache_misses': misses,
                        'cache_accepts': accepts,
                        'cache_rejects': rejects,
                        'ltm_entries': ltm_entries,
                        'ltm_seeded': seeded_count,
                        'ltm_promoted': promoted,
                        'ltm_pruned': pruned,
                    }
                    all_results.append(rec)

                    log.info(f"    Period {period:2d} | obj: {best_obj:.4f} | "
                             f"cov: {pct_covered:.1f}% | time: {runtime:.2f}s | "
                             f"hits: {hits} | seeded: {seeded_count} | LTM: {ltm_entries}")

    return all_results


