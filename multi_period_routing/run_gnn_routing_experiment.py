    prefix = args.area if args.area else 'all'

    # ── Raw results ───────────────────────────────────────────────────────────
    all_xlsx = os.path.join(out_dir, f'{prefix}_results.xlsx')
    df_all.to_excel(all_xlsx, index=False)
    log.info(f"Results saved to {all_xlsx}")

